"""
Estrctura selectiva simple (if)
script en Python que solicite la edad del ususario y si esa edad es mayor o
igual que 18 indicarle que ya es maor de edad.
"""
edad = int(input(" Hola ¿cuantos años tienes? "))

if edad >= 18:
    print(" tenes pelos en los huevos...")
    print(" y en el culo tambien")
if edad < 18:
    print( "te faltan pelos en los huevos chiquto")
